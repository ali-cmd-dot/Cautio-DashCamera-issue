// Returns [lat, lng] for Leaflet  (note: stored as [lng, lat])
export function findCityCoords(cityName) {
  if (!cityName) return null;
  const name = cityName.trim();

  // Exact match
  if (CITY_COORDS[name]) {
    const [lng, lat] = CITY_COORDS[name];
    return [lat, lng];
  }

  // Case-insensitive exact
  const lower = name.toLowerCase();
  for (const [key, coords] of Object.entries(CITY_COORDS)) {
    if (key.toLowerCase().trim() === lower) {
      const [lng, lat] = coords;
      return [lat, lng];
    }
  }

  // Partial match
  for (const [key, coords] of Object.entries(CITY_COORDS)) {
    const k = key.toLowerCase().trim();
    if (k.includes(lower) || lower.includes(k)) {
      const [lng, lat] = coords;
      return [lat, lng];
    }
  }

  return null;
}
